        <p><b>Баярто Дамбаев о балете «Красавица Ангара»‎</b></p>
<p>«Над «Красавицей Ангарой»‎ работали великие люди, — рассказывает главный балетмейстер БГАТОиБ Баярто Дамбаев. — Лев Книппер и Бау Ямпилов, Михаил Заславский и Игорь Моисеев… Была проведена большая предварительная научная работа: историческая, этнографическая. Именно поэтому балет актуален до сих пор. Он идёт на нашей сцене уже больше пятидесяти лет и ни разу не выходил из репертуара. На нем выросли все наши титулованные артисты, все ведущие мастера сцены: Абашеев, Васильев, Хараев, Баранов, после них — Ганженко, Муруев, Павленко… Из тех, кто танцует сейчас,  — Батор Надмитов, затем Булыт Раднаев, Михаил Овчаров, Виктор Дампилов, Баир Цыдыпылов, Баир Жамбалов, Артемий Плюснин… Это наш балет.  Я очень горжусь им, я  знаю, что и наша публика, и публика в Чите и Иркутске любит этот балет. Я бы хотел, чтоб его ставили и в других городах. Подобно татарскому «‎Шурале», который шел на многих сценах России, в Средней Азии, в Прибалтике… ‎Так будет происходить популяризация нашей национальной культуры: через танец люди будут узнавать о Бурятии, о Байкале, об Ангаре…</p>
<p>Сейчас идет постановка Морихиро Иваты 2016-го года. Возможно, через некоторое время мы вернемся к историческому варианту. Это будет интересно для зрителей, особенно тех, кто пришел в театр недавно. Впрочем, изменения Морихиро касались в основном декорационных и мизансценических решений: основа балета осталась прежней, и многие номера мы оставили в первозданном виде. Например, адажио Моисеева из третьего акта — это раритет. Чем больше времени проходит, тем оно дороже становится, это адажио. Когда мы его репетируем — то вообще ничего не меняем, оставляем всё, как было: каждую ноту, движение глаз, пор-де-бра… Мы, педагоги, бережно, по крупицам собираем это наследие — и передаем его следующим поколениям».</p>   </div>

