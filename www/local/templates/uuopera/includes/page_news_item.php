<p>Праздник был наполнен не только награждениями, но и яркими выступлениями. Зажигательные капустники от разных театров — Бурятского театра оперы и балета, театра кукол «Ульгэр», Бурятского театра драмы имени Хоца Намсараева, Русского драматического театра имени Николая Бестужева и Молодежного художественного театра — продемонстрировали творческий потенциал и искрометный юмор артистов, подарив зрителям минуты настоящего смеха и радости.&nbsp;</p>



