<p>В Номинации «Служитель театра»  одним из лауреатов признан  Юрий Муруев — балетмейстер Бурятского театра оперы и балета, народный артист РСФСР. За лучшую балетную партию Модеста Алексеевича в балете «Анюта» (12+) на музыку Валерия Гаврилина  награждён заслуженный артист Бурятии Михаил Овчаров. </p>



