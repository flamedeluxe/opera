                    <h1 class="text-h1">Браво! Валентина Цыдыпова и Ольга Аюрова стали лауреатами государственной премии Республики Бурятия</h1>
                    <div class="text-p3">31.03.2026</div>
                    <div class="text-p1">
                        <p>Торжественную церемонию вручения государственных премий провела заместитель председателя Правительства Бурятии — министр здравоохранения Республики Бурятия Евгения Лудупова в рамках шоу «БРАВО-2026» в честь Международного дня театра и 150-летия Союза театральных деятелей 27 марта.</p>                    </div>
