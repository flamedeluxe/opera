<p>К поздравлениям присоединился первый заместитель министра культуры Владимир Вениаминович Кожевников. Он с теплотой обратился к присутствующим, поздравил их с профессиональным праздником и выразил искреннюю признательность за неоценимый труд, преданность искусству и неустанное стремление к творческому совершенству.&nbsp;</p>



<p>Заместитель председателя Правительства Бурятии — министр здравоохранения Республики Бурятия Евгения Лудупова вручила Государственные премии Республики Бурятия в области театрального искусства, которые были присуждены двум выдающимся личностям, легендарным оперным певицам &#8212; заслуженной артистке Российской Федерации Валентине Цыдыповой и заслуженной артистке РСФСР Ольге Аюровой. Их многолетний труд, преданность сцене и создание уникальных художественных образов стали эталоном для молодого поколения артистов, а сохранение и развитие театральных традиций республики — их неоспоримая заслуга.&nbsp;</p>



